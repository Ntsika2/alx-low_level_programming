#include <stdio.h>

/**
 * add - add two integers
 * @a: first integer
 * @b: second integer
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtract two integers
 * @a: first integer
 * @b: second integer
 * Return: difference
 */
int sub(int a, int b)
{
     return (a - b);
}
/**
 * mul - multiplies two integers
 * @a: first integer
 * @b: second integer
 * Return: product
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divide two integers
 * @a: first integer
 * @b: second integer
 * Return: Qoutient
 */
int div(int a, int b)
{
        return (a / b);
}
/**
 * mod - modulus of two integers
 * @a: first integer
 * @b: second integer
 * Return: remainder
 */
int mod(int a, int b)
{
        return (a % b);
}
